#include <stdio.h>

int	main(void)
{
	int	x;

	x = 0;
	printf("Enter a number: ");
	scanf("%d", &x);
	if ((x % 2) == 0)
	{
		printf("%d is: even\n", x);
	}
	else if ((x % 2) == 1)
	{
		printf("%d is odd\n", x);
	}
	return (0);
}
