#include <stdio.h>

int	main(void)
{
	int	arr[9] = {34, 56, 54, 32, 67, 89, 90, 32, 21};
	int	i;

	i = 0;
	while (i <= 8)
	{
		printf("%d", arr[i]);
		if (i < 8)
		{
			printf(" ,");
		}
		i++;
	}
	printf("\n");
	i -= 1;
	while (i >= 0)
	{
		printf("%d", arr[i]);
		if (i > 0)
		{
			printf(" ,");
		}
		i--;
	}
}
