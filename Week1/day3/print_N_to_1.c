#include <stdio.h>

int	main(void)
{
	int	n;
	int	i;

	i = 1;
	printf("Enter a number: ");
	scanf("%d", &n);
	while (n >= i)
	{
		printf("%d\n", n);
		n--;
	}
	return (0);
}
