#include <stdio.h>

int	main(void)
{
	int	arr[3][2][4] = {{{1, 2, 3, 4}, {5, 6, 7, 8}},
				{{9, 8, 7, 6}, {5, 4, 3, 2}},
				{{0, 3, 2, 6}, {8, 7, 9, 9}}};
	int	i;
	int	j;
	int	k;
	i = 0;
	j = 0;
	k = 0;
	while (i < 3)
	{
		while (j < 2)
		{
			while (k < 4)
			{
				printf("%d ", arr[i][j][k]);
				k++;
			}
			k = 0;
			printf("\t");
			j++;
		}
		j = 0;
		printf("\n");
		i++;
	}
}
